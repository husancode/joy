public class JavaBean {
	
	private String name;
	
	private int age;
	
	private String areo;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getAreo() {
		return areo;
	}

	public void setAreo(String areo) {
		this.areo = areo;
	}
	

}
